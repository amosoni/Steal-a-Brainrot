export const getRarityText = (rarity: number) => {
  switch (rarity) {
    case 1: return 'Common'
    case 2: return 'Rare'
    case 3: return 'Epic'
    case 4: return 'Legendary'
    case 5: return 'Mythic'
    case 6: return 'Ancient'
    case 7: return 'Divine'
    default: return 'Unknown'
  }
}
